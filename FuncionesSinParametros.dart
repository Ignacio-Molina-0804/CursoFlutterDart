// Definición de variables globales

int valor1 = 50, valor2 = 100;

// Definición de función sin parámetros que retorna un entero
int sumar() {
  return valor1 + valor2;
}

// Definición de función sin parámetros que retorna una cadena
String DimeNombre() {
  return "IgnacioMolina";
}

// Función principal
void main() {
  print(sumar());
  print(DimeNombre());
}
