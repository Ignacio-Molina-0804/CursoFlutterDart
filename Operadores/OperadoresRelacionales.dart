void main() {
  int valor1 = 90, valor2 = 55;

  // Operador de Igualdad
  print(valor1 == valor2);

  // Operador de Desigualdad
  print(valor1 != valor2);

  // Operador Mayor que
  print(valor1 > valor2);

  // Operador Menor que
  print(valor1 < valor2);

  // Operador Mayor o Igual que
  print(valor1 >= valor2);

  // Operador Menor o Igual que
  print(valor1 <= valor2);
}
